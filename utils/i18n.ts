export const t = (lang: string, key: string): string => {
  const k = key as keyof typeof translations['en'];
  return translations[lang as 'en' | 'zh']?.[k] || translations['en'][k] || key;
};

export const formatTimeAgo = (minutes: number, lang: string): string => {
  if (minutes < 1) return lang === 'zh' ? '刚刚' : 'Just now';
  if (minutes < 60) return lang === 'zh' ? `${minutes}分钟前` : `${minutes}m ago`;
  const hours = Math.floor(minutes / 60);
  if (hours < 24) return lang === 'zh' ? `${hours}小时前` : `${hours}h ago`;
  const days = Math.floor(hours / 24);
  return lang === 'zh' ? `${days}天前` : `${days}d ago`;
};
